'''
    The program displays the first 50 integers from 0 (0-49), and shows the square of that integer.
    Steps:
    Set a variable i equal to 0.
    While this variable i is less than 50, print out the value of i and the square of i,
    incrementing the variable i on each loop.
'''

i = 0

while i < 50:
    print("The number is:", i, "and the square of", i, "is:", i**2)
    i += 1
