# Using mathematical expressions within print statements

length = 064885.87

print('Area of square with a length of 064885.87  = ', length**2)

print('Area of cube with a length of 064885.87  = ', length**3)

print('Area of a circle with diameter of 064885.87 = ', ((length/2)**2)*3.1415927)

print('Area of a sphere with diameter of 064885.87 = ', 4*((length/2)**2)*3.1415927)

print('Area of a cylinder with diameter of 064885.87 and side of 064885.87 = ', (2*3.1415927*(length/2)*length) +
      (2*3.1415927*((length/2)**2)))

